    private void showDataTable(List<KhachHang> listkh) {
        dtmkh.setRowCount(0);
        for (KhachHang kh : listkh) {
            dtmkh.addRow(new Object[]{kh.getTenKH(), kh.getGioiTinh(kh.getGioiTinh()), kh.getSdt()});
        }
    }

    private boolean kiemTraDuLieu() {
        if (txtSdt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "nhap du du lieu");
            return false;
        } else if (!txtSdt.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Nhap sai Du lieu");
            return false;
        }
        return true;
    }


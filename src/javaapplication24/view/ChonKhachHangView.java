    private void btnTimKiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemMouseClicked
        // TODO add your handling code here:
        if (txtTimKiem.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nhap sdt can tim");
        } else {
            List<KhachHang> list = new ArrayList<>();
            for (KhachHang kh : listkh) {
                if (kh.getSdt().equals(txtTimKiem.getText())) {
                    list.add(kh);
                }
            }
            showDataTable(list);
        }
    }//GEN-LAST:event_btnTimKiemMouseClicked

    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        if (kiemTraDuLieu()) {
            KhachHang kh = new KhachHang(txtTenKhachHang.getText(), rdoNu.isSelected() ? 1 : 0, txtSdt.getText());
            JOptionPane.showMessageDialog(this, khs.add(kh));
            listkh = khs.getData();
            showDataTable(listkh);
        }
    }//GEN-LAST:event_btnThemMouseClicked


    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        // TODO add your handling code here:
        if(kiemTraDuLieu()){
            int count =0;
            for(QLNhanVien nv:list){
                if(nv.getSDT().equalsIgnoreCase(txtUserName.getText())&&nv.getMatKhau().equalsIgnoreCase(txtPassword.getText())){
                    MainJFrame mf = new MainJFrame(nv);
                    count++;
                    mf.setVisible(true);
                    this.dispose();
                }
            }
            if(count==0){
                JOptionPane.showMessageDialog(this,"Nhap sai nhap lai");
            }
        }
    }//GEN-LAST:event_btnLoginMouseClicked

